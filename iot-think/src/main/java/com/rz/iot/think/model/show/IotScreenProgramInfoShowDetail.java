public class IotScreenProgramInfoShowDetail {

    // 节目id
    private Integer id;
    // 节目名称
    private String name;
    // 节目高
    private Integer height;
    // 节目宽
    private Integer width;
    // 节目总大小
    private Integer totalSize;
    // 节目总长度
    private Integer programLength;
    // 节目分辨率
    private String resolution;

    private Integer type;
    // 创建用户
    private Integer createAdmin;

    private Integer verify;

    private String describe;

    private Integer status;

    // 节目布局信息（包含层级信息）
    private List<IotScreenProgramLayer> programLayers;

    public IotScreenProgramInfoShowDetail() {
    }

    public IotScreenProgramInfoShowDetail(Integer id, String name, Integer height, Integer width, Integer totalSize, Integer programLength, String resolution, Integer type, Integer createAdmin, Integer verify, String describe, Integer status) {
        this.id = id;
        this.name = name;
        this.height = height;
        this.width = width;
        this.totalSize = totalSize;
        this.programLength = programLength;
        this.resolution = resolution;
        this.type = type;
        this.createAdmin = createAdmin;
        this.verify = verify;
        this.describe = describe;
        this.status = status;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getHeight() {
        return height;
    }

    public void setHeight(Integer height) {
        this.height = height;
    }

    public Integer getWidth() {
        return width;
    }

    public void setWidth(Integer width) {
        this.width = width;
    }

    public Integer getTotalSize() {
        return totalSize;
    }

    public void setTotalSize(Integer totalSize) {
        this.totalSize = totalSize;
    }

    public Integer getProgramLength() {
        return programLength;
    }

    public void setProgramLength(Integer programLength) {
        this.programLength = programLength;
    }

    public String getResolution() {
        return resolution;
    }

    public void setResolution(String resolution) {
        this.resolution = resolution;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public Integer getCreateAdmin() {
        return createAdmin;
    }

    public void setCreateAdmin(Integer createAdmin) {
        this.createAdmin = createAdmin;
    }

    public Integer getVerify() {
        return verify;
    }

    public void setVerify(Integer verify) {
        this.verify = verify;
    }

    public String getDescribe() {
        return describe;
    }

    public void setDescribe(String describe) {
        this.describe = describe;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public List<IotScreenProgramLayer> getProgramLayers() {
        return programLayers;
    }

    public void setProgramLayers(List<IotScreenProgramLayer> programLayers) {
        this.programLayers = programLayers;
    }
}
