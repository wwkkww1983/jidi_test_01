public class IotPowerParam {
    //周期模式 1.日 2.月 3.年
    private String cycleMode;
    //集中器序列号
    private String sn;
    //单灯控制器
    private Integer singleLightId;
    //路灯
    private Integer streetLightId;
    //路名
    private Integer streetId;
    //区
    private Integer areaId;

    public String getCycleMode() {
        return cycleMode;
    }

    public void setCycleMode(String cycleMode) {
        this.cycleMode = cycleMode;
    }

    public String getSn() {
        return sn;
    }

    public void setSn(String sn) {
        this.sn = sn;
    }

    public Integer getSingleLightId() {
        return singleLightId;
    }

    public void setSingleLightId(Integer singleLightId) {
        this.singleLightId = singleLightId;
    }

    public Integer getStreetLightId() {
        return streetLightId;
    }

    public void setStreetLightId(Integer streetLightId) {
        this.streetLightId = streetLightId;
    }

    public Integer getStreetId() {
        return streetId;
    }

    public void setStreetId(Integer streetId) {
        this.streetId = streetId;
    }

    public Integer getAreaId() {
        return areaId;
    }

    public void setAreaId(Integer areaId) {
        this.areaId = areaId;
    }
}
