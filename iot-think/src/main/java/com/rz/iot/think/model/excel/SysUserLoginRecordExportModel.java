package com.rz.iot.think.model.excel;

import com.alibaba.excel.annotation.ExcelProperty;
import com.alibaba.excel.metadata.BaseRowModel;

import java.util.Date;

public class SysUserLoginRecordExportModel extends BaseRowModel {
    @ExcelProperty(value = {"用户名", "用户名", "用户名"}, index = 0)
    private String name;

    @ExcelProperty(value = {"IP", "IP", "IP"}, index = 1)
    private String ip;

    @ExcelProperty(value = {"MAC", "MAC", "MAC"}, index = 2)
    private String mac;

    @ExcelProperty(value = {"登录类型", "登录类型", "登录类型"}, index = 3)
    private String login_type;

    @ExcelProperty(value = {"Web/App", "Web/App", "Web/App"}, index = 4)
    private String login_mode;

    @ExcelProperty(value = {"创建时间", "创建时间", "创建时间"}, index = 5)
    private Date createTime;

    @ExcelProperty(value = {"更新时间", "更新时间", "更新时间"}, index = 6)
    private Date updateTime;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getMac() {
        return mac;
    }

    public void setMac(String mac) {
        this.mac = mac;
    }

    public String getLogin_type() {
        return login_type;
    }

    public void setLogin_type(String login_type) {
        this.login_type = login_type;
    }

    public String getLogin_mode() {
        return login_mode;
    }

    public void setLogin_mode(String login_mode) {
        this.login_mode = login_mode;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
}