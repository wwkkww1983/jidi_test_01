/**
 * 描述:
 * 摄像头列表
 *
 * @author Zhouyz
 * @create 2019-04-04 16:37
 */

public class MonitorList {
    //摄像头名称
    private String monitorName;
    //摄像头sn
    private String monitorSn;
    //摄像头型号
    private String monitorModelNumber;
    //路灯名称
    private String streetlightName;
    //摄像头状态
    private String monitorStatus;
    //更新时间
    private String updateTime;
    //摄像头厂商
    private String manufacturer;

    public String getMonitorName() {
        return monitorName;
    }

    public void setMonitorName(String monitorName) {
        this.monitorName = monitorName;
    }

    public String getMonitorSn() {
        return monitorSn;
    }

    public void setMonitorSn(String monitorSn) {
        this.monitorSn = monitorSn;
    }

    public String getMonitorModelNumber() {
        return monitorModelNumber;
    }

    public void setMonitorModelNumber(String monitorModelNumber) {
        this.monitorModelNumber = monitorModelNumber;
    }

    public String getStreetlightName() {
        return streetlightName;
    }

    public void setStreetlightName(String streetlightName) {
        this.streetlightName = streetlightName;
    }

    public String getMonitorStatus() {
        return monitorStatus;
    }

    public void setMonitorStatus(String monitorStatus) {
        this.monitorStatus = monitorStatus;
    }

    public String getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(String updateTime) {
        this.updateTime = updateTime;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }
}
